# --- Code Detection & Syntax Surgeon Utils ---
def detect_python_code(text):
    code_patterns = [
        r'def\s+\w+\s*\(', r'class\s+\w+', r'import\s+\w+',
        r'from\s+\w+\s+import', r'if\s+.*:', r'for\s+.*in\s+', r'while\s+.*:'
    ]
    return any(re.search(pattern, text) for pattern in code_patterns)

def check_syntax(code):
    try:
        ast.parse(code)
        return True, None
    except SyntaxError as e:
        return False, f"Syntax Error (line {e.lineno}): {e.msg}"
    except Exception as e:
        return False, f"Error: {str(e)}"

def fix_code_indentation(code):
    lines = code.split('\n')
    fixed_lines = [line.rstrip().replace('\t', '    ') for line in lines]
    return '\n'.join(fixed_lines)

def fix_code_brackets(code):
    for pair in [('(', ')'), ('[', ']'), ('{', '}')]:
        open_c = code.count(pair[0])
        close_c = code.count(pair[1])
        if open_c > close_c:
            code += pair[1] * (open_c - close_c)
    return code

def format_code(code):
    try:
        return black.format_str(code, mode=black.FileMode())
    except:
        try:
            return autopep8.fix_code(code)
        except:
            return code

def fix_python_code(code):
    code = fix_code_indentation(code)
    code = fix_code_brackets(code)
    is_valid, error_msg = check_syntax(code)
    if not is_valid:
        return {"success": False, "fixed_code": code, "error": error_msg, "message": f"⚠️ Syntax Error: {error_msg}"}
    code = format_code(code)
    return {"success": True, "fixed_code": code, "error": None, "message": "✅ Code fixed successfully!"}

# --- PDF Export Logic ---
def export_chat_pdf():
    if not conversation_history:
        return "❌ Chat is empty!", None

    filename = f"chat_history_{datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"
    doc = SimpleDocTemplate(filename, pagesize=letter)
    styles = getSampleStyleSheet()

    title_style = ParagraphStyle('TitleStyle', parent=styles['Title'], textColor=colors.hexColor('#2c3e50'), spaceAfter=12)
    user_style = ParagraphStyle('UserStyle', parent=styles['Normal'], textColor=colors.blue, leftIndent=10)
    bot_style = ParagraphStyle('BotStyle', parent=styles['Normal'], textColor=colors.darkgreen, leftIndent=10)

    story = []
    story.append(Paragraph("⚪ Granite Chatbot - Chat History", title_style))
    story.append(Paragraph(f"<b>Exported on:</b> {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", styles['Normal']))
    story.append(Spacer(1, 0.2*inch))

    for i, msg in enumerate(conversation_history, 1):
        role = msg['role'].upper()
        content = msg['content'].replace('<', '&lt;').replace('>', '&gt;')

        story.append(Paragraph(f"<b>{role}:</b>", user_style if role == "USER" else bot_style))
        story.append(Paragraph(content, styles['Normal']))
        story.append(Spacer(1, 0.1*inch))

        if i % 5 == 0:
            story.append(PageBreak())

    story.append(Spacer(1, 0.3*inch))
    story.append(Paragraph("<b>End of Chat</b>", styles['Normal']))

    try:
        doc.build(story)
        return f"✅ PDF Generated: {filename}", filename
    except Exception as e:
        return f"❌ Error: {str(e)}", None

