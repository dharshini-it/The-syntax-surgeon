# --- Main Chat Logic ---
def chat_response(message, history, temp, tokens, top_p):
    global conversation_history
    if not message.strip():
        return history, ""

    conversation_history.append({"role": "user", "content": message})

    if detect_python_code(message):
        fix_res = fix_python_code(message)
        response = f"{fix_res['message']}\n\n**Processed Code:**\n```python\n{fix_res['fixed_code']}\n```"
    else:
        out = model_pipe(message, max_new_tokens=tokens, temperature=temp, top_p=top_p, do_sample=True)
        response = out[0]['generated_text']

    conversation_history.append({"role": "assistant", "content": response})
    history.append((message, response))
    return history, ""

